"""Render eval results to a markdown report."""
from __future__ import annotations

import subprocess
from datetime import datetime, timezone
from pathlib import Path

from hdb_rag.eval.runner import CaseResult


def _pct(passed: int, total: int) -> str:
    return f"{(100 * passed / total):.0f}%" if total else "—"


def _summary_row(label: str, results: list[CaseResult]) -> str:
    def cnt(attr: str) -> tuple[int, int]:
        applicable = [getattr(r, attr) for r in results if getattr(r, attr) is not None]
        return sum(1 for v in applicable if v), len(applicable)

    r_p, r_t = cnt("retrieval_pass")
    f_p, f_t = cnt("faithfulness_pass")
    rl_p, rl_t = cnt("relevance_pass")
    c_p, c_t = cnt("citation_pass")
    rf_p, rf_t = cnt("refusal_pass")
    return (
        f"| {label} | {_pct(r_p, r_t)} | {_pct(f_p, f_t)} | "
        f"{_pct(rl_p, rl_t)} | {_pct(c_p, c_t)} | {_pct(rf_p, rf_t)} |"
    )


def _commit_hash() -> str:
    try:
        return subprocess.check_output(["git", "rev-parse", "--short", "HEAD"]).decode().strip()
    except Exception:
        return "unknown"


def render_report(results_by_mode: dict[str, list[CaseResult]], out_path: Path) -> None:
    when = datetime.now(timezone.utc).isoformat()
    lines: list[str] = []
    lines.append(f"# Eval Report — {when[:10]} (commit `{_commit_hash()}`)\n")
    lines.append("## Summary\n")
    lines.append("| Mode | Retrieval@5 | Faithfulness | Relevance | Citation | Refusal |")
    lines.append("|---|---|---|---|---|---|")
    for mode, results in results_by_mode.items():
        lines.append(_summary_row(mode, results))
    lines.append("")

    primary = "best" if "best" in results_by_mode else next(iter(results_by_mode))
    lines.append(f"## Failures ({primary} mode)\n")
    failures: list[str] = []
    for r in results_by_mode[primary]:
        flags = []
        if r.retrieval_pass is False:
            flags.append("retrieval")
        if r.faithfulness_pass is False:
            flags.append("faithfulness")
        if r.relevance_pass is False:
            flags.append("relevance")
        if r.citation_pass is False:
            flags.append("citation")
        if r.refusal_pass is False:
            flags.append("refusal")
        if flags:
            failures.append(
                f"- **#{r.id}** ({r.category}, failed: {', '.join(flags)}): "
                f"_{r.question}_\n  > {r.answer[:200]}…"
            )
    lines.extend(failures or ["_No failures._"])
    lines.append("")
    lines.append("## Notes\n")
    lines.append("- Eval set: `data/eval_set.yaml` (30 cases).")
    lines.append("- Generated by `make eval`.")

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(lines))
